import turtle

turtle.up()
turtle.goto(-150, -50)
turtle.down()
turtle.forward(300)

turtle.up()
turtle.goto(-150, 50)
turtle.down()
turtle.forward(300)

turtle.up()
turtle.left(90)
turtle.goto(-50, -150)
turtle.down()
turtle.forward(300)

turtle.up()
turtle.goto(50, -150)
turtle.down()
turtle.forward(300)

turtle.hideturtle()

turtle.done()
