# An example of using a variable.

# The following line stores the input from
# the user into the variable called 'name'
name = input("What is your name?")

# Use print() to show what is stored in 'name'
print(name)

# Display it together with other text
print("Your name is", name)

# If you look at the result of the last line and 
# the next line you will see that spaces are 
# automatically added for you by Python, around the name
print("Your name is", name, "and that's a great name!")
